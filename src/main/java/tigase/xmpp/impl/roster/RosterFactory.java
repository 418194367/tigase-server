public abstract class RosterFactory {

	private static RosterAbstract shared = null;

	public static RosterAbstract getRosterImplementation(boolean shared_impl) {
		if (shared_impl) {
			if (shared == null) {
				shared = new RosterFlat();
			}
			return shared;
		}
		return new RosterFlat();
	}

}
